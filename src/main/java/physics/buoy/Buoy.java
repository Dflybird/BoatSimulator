package physics.buoy;

public class Buoy {

    public double x;
    public double y;
    public double z;
    public double radius;
    public double weight;

    public Buoy(double x, double y, double z, double radius, double weight) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.radius = radius;
        this.weight = weight;
    }
}
